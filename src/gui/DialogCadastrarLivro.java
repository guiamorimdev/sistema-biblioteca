    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String titulo = campoTitulo.getText().trim();
        String autor = campoAutor.getText().trim();
        String anoStr = campoAno.getText().trim();
        String paginasStr = campoPaginas.getText().trim();
        String isbn = campoIsbn.getText().trim();
        String quantidadeStr = campoQuantidade.getText().trim();

        if (titulo.isEmpty() || autor.isEmpty() || anoStr.isEmpty() || 
            paginasStr.isEmpty() || isbn.isEmpty() || quantidadeStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int ano = Integer.parseInt(anoStr);
            int paginas = Integer.parseInt(paginasStr);
            int quantidade = Integer.parseInt(quantidadeStr);
            gerenciador.adicionarItem(new Livro(titulo, autor, ano, paginas, isbn, quantidade));
            JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");
            dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ano, páginas e quantidade devem ser números!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

